using System;
using System.Collections.Generic;
using System.Text;

namespace MathMethods
{
    class Math
    {
        public static float Halve(int input)
        {
            float total = input / 2;
            return total;
        }

        public static float timesTen(int input)
        {
            float total = input * 10;
            return total;
        }

        public static float Squared(int input)
        {
            float total = input * input;
            return total;
        }
    }
}
